"""Módulo para demostración de suma.

Este módulo provee una función de suma que toma dos enteros y retorna su suma.
"""

def suma(a: int, b: int) -> int:
    """Suma dos números enteros.

    Args:
        a (int): Primer número entero.
        b (int): Segundo número entero.

    Returns:
        int: La suma de `a` y `b`.
    """
    # La variable `x` ha sido removida ya que no se usa,
    # lo cual mejora la claridad y el rendimiento del código.
    return a + b

